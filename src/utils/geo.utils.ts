/**
 * Great-circle distance between two lat/lng points, in kilometres.
 */
export function distanceKm(
  lat1: number,
  lng1: number,
  lat2: number,
  lng2: number
): number {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLng = ((lng2 - lng1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLng / 2) *
      Math.sin(dLng / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

// Collector is considered "arrived" once within this radius of the
// resident's pickup pin. 120m comfortably absorbs typical GPS drift
// (phones) while still meaning "at the location", not "in the area".
export const ARRIVAL_RADIUS_KM = 0.12;